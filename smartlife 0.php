						<h2 id="section_further_down_0"><strong>Installer le programme</strong></h2>
						
						<p>Que vous soyez sur android ou IOS cela reste la même procédure.</p>
						<p>Seul l’interface pourrait esthétiquement légèrement changer.</p>
						<p>Comme je suis sur Android, je vous montre une installation via le Play store.</p>
						<p><a href="https://play.google.com/store/apps/details?id=com.tuya.smartlife&hl=fr&gl=US" target="_blank" rel="noopener noreferrer"><font color="blue"><u>Téléchargez et installez SMART LIFE depuis le play store</u></font></a></p>
						<br>
						
						<br><center><img src="images/smartlife/smartlife 0/img 2.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Ouvrez l’application sur votre smartphone ou tablette en sélectionnant l’icone Smart Life.</p>
						<br>
						<br>

						<h2 id="section_further_down_1"><strong>Créer votre compte utilisateur</strong></h2>

						<p>L’application s’ouvre.</p>
						<p>Vous avez le choix entre créer un compte ou vous connecter à un compte existant.</p>
						<p>Si vous n’avez pas encore de compte, sélectionnez <strong>“Créer un nouveau compte”</strong>.</p>

						<br><center><img src="images/smartlife/smartlife 0/img 3.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>La page de politique de confidentialité s’ouvre, vous devez marquer votre accord en sélectionnant <strong>“D’accord”</strong></p>
						
						<br><center><img src="images/smartlife/smartlife 0/img 4.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Il vous est alors demandé quelques renseignements.</p>
						<p>Choisissez votre localisation en premier lieu (normalement c’est automatique si votre smartphone est bien paramétré)</p>
						<p>Inscrivez dans la case <strong>“courriel”</strong> votre adresse email, un code d’activation vous serra envoyé lorsque vous aurez validé en sélectionnant <strong>“Obtenir le code de vérification”</strong>.</p>
						<br>
						<p>Notez que vous pouvez aussi choisir une inscription par numéro de téléphone (via l’autre onglet), dans ce cas un SMS vous sera envoyé pour valider le compte.</p>

						<br><center><img src="images/smartlife/smartlife 0/img 5.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Allez vérifier votre boite email afin de pouvoir entrer le code de validation.</p>

						<br><center><img src="images/smartlife/smartlife 0/img 6.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Une fois l’étape de validation effectuée, il vous serra demandé de choisir un mot de passe pour votre compte.</p>
						
						<br><center><img src="images/smartlife/smartlife 0/img 7.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>C’est fini.</p>
						<p>Votre compte est créé, validé et votre application prête à accueillir vos premiers objets connectés.</p>
						
						<br><center><img src="images/smartlife/smartlife 0/img 8.png" width="20%" height="20%" alt=""/></center><br>
						<br>
						<br>

						<h2 id="section_further_down_2"><strong>Ajouter un objet connecté</strong></h2>
						
						<p>C’est maintenant qu’on entre dans le vif du sujet…</p>
						<p>Nous allons ajouter un objet connecté dans l’interface afin de pouvoir le contrôler depuis l’application.</p>
						<p>Nous allons installer une prise connectée (wifi smart plug) mais la procédure est valable pour tout objet connecté compatible SMART LIFE / TUYA.</p>
						<p>Sélectionnez le symbole <strong>“+”</strong> dans le coin supérieur droit de l’application.</p>
						<br>
						<p>Nb: La première fois que vous utilisez l’application il est également possible de sélectionner le bouton bleu <strong>“Ajouter”</strong> au milieu de l’écran.</p>
						
						<br><center><img src="images/smartlife/smartlife 0/img 9.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>La liste des périphériques compatible s’ouvre.</p>
						<p>Elle est classée par catégories de fonctionnement, à vous de retrouver votre type d’objet.</p>
						<p>Notez qu’il n’y a pas de marques ou de modèles, seul est défini le type d’objet… vous ne pourrez donc pas vous tromper !</p>
						
						<br><center><img src="images/smartlife/smartlife 0/img 10.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Pour ma part ce sera l’ajout d’une prise connectée en wifi, je choisis donc de sélectionner la première icone <strong>“Prise (Wi-Fi)”</strong>.</p>
						<p>Le programme me demande maintenant de passer mon objet connecté en mode appairage.</p>
						<p>La plupart du temps il faut presser et maintenir le bouton d’allumage de l’objet pendant minimum 5 secondes pour que le mode appairage soit initié, en général la led d’état de l’objet se met à clignoter pour le renseigner; Cependant, parfois cette procédure change un peu, pensez donc à lire le manuel utilisateur qui renseignera la meilleure manière de procéder (ex: les ampoules connectées il faut allumer et éteindre 5x rapidement pour les passer en appairage)</p>
						<br>
						<p>Note: en général, un objet connecté jamais mis sous tension se place en mode appairage lors de la première mise sous tension.</p>
						<br>
						<p>Lorsque votre objet est en mode appairage, sélectionnez le bouton bleu <strong>“Voyant de confirmation dans le flash”</strong>. Cela ne veut pas dire grand chose, il s’agit probablement d’une traduction chinois – anglais – français</p>
						
						<br><center><img src="images/smartlife/smartlife 0/img 11.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Une nouvelle fenêtre s’ouvre, demandant de renseigner les informations de son réseau Wi-Fi domestique.</p>
						<p><strong>Attention: 99% du temps les objets connectés ne supportent que les réseaux 2.4GHz et pas ceux en 5GHz</strong>, pensez à bien choisir le bon réseau Wi-Fi domestique.</p>
						<p>Soit vous êtes déjà sur le bon réseau (dans cet exemple le SSID de mon réseau 2.4GHz est Homelab), soit vous êtes sur le mauvais, dans ce cas sélectionnez l’icone à droite qui représente deux petites flèches l’une sur l’autre.</p>
						<p>Indiquez ensuite votre mot de passe Wi-Fi (note: pour visualiser en clair votre mot de passe wifi, vous pouvez sélectionner l’icone ressemblant à un œil fermé situé à droite sur la même ligne.</p>
						<br>
						
						<br><center><img src="images/smartlife/smartlife 0/img 12.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Le programme va chercher votre objet connecté sur le réseau wifi.</p>
						<p>L’opération peut prendre un peu de temps…</p>

						<br><center><img src="images/smartlife/smartlife 0/img 13.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Une fois qu’il a trouvé l’objet, cela va très vite, il l’enregistre et il le pré-configure pour vous.</p>

						<br><center><img src="images/smartlife/smartlife 0/img 14.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Il vous proposera ensuite de choisir un nom pour cet objet.</p>
						<p>Soit vous laissez le nom d’origine, soit vous écrivez votre propre nom d’objet (recommandé).</p>
						<p>Pensez à bien choisir le nom pour facilement le retrouver dans la liste des périphériques, vous pourrez aussi l’énoncer pour le contrôler à la voix.</p>
						<p>Validez en sélectionnant le bouton bleu <strong>“Terminé”</strong></p>

						<br><center><img src="images/smartlife/smartlife 0/img 15.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>C’est fini !!!</p>
						<p>Votre objet connecté est créé, enregistré et pré-configuré dans l’application.</p>
						<p>Il est maintenant prêt à être utilisé !</p>

						<br><center><img src="images/smartlife/smartlife 0/img 16.png" width="20%" height="20%" alt=""/></center><br>
						
						<p>Si vous sélectionnez la petite flèche dans le coin supérieur gauche, vous retournez à l’accueil de l’application, là où se trouve votre liste d’objets connectés.</p>
						<p>Quand vous en avez plusieurs, voici à quoi cela ressemble…</p>

						<br><center><img src="images/smartlife/smartlife 0/img 17.png" width="20%" height="20%" alt=""/></center><br>
						<br>
						<br>

						<h2  id="section_further_down_3"><strong>Utiliser votre objet connecté</strong></h2>

						<p>Là ça devient nettement plus compliqué à vous expliquer… car chaque objet a des fonctions spécifiques!</p>
						<p>Ainsi une prise ou une ampoule n’est pas l’autre et dépendra directement du modèle ou de la marque.. vous pouvez par exemple avoir une prise avec certaines fonctions qu’une même prise d’une autre marque n’aura pas.</p>
						<p>Il vous faudra donc expérimenter par vous même à l’utilisation.</p>
						<br>
						<p>Pour entrer dans les fonctions de votre objet connecté, sélectionnez le dans la liste des périphériques sur l’accueil de l’application et explorez</p>
						<br>
						<p>Bon amusement !</p>

