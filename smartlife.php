						<h1>Tutoriel Smart Life : comment utiliser le « couteau Suisse » de la maison connectée ? </h1>
						
						<blockquote>
							Tutoriel d'installation et d'utilisation pour Tuya / Smart Life
						</blockquote>

						<div class="clearfix post-meta">
							<p><span><i class="fa fa-user"></i> Admin</span> <span><i class="fa fa-clock-o"></i> 18 Jui 2019</span> <span><i class="fa fa-comment"></i> 4 comments</span> <span><i class="fa fa-folder"></i> Smartlife</span></p>
						</div>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-clock-o"></i> Mise à jour : 16 Nov 2021</span></p>
						</div>

						<p>Si vous êtes sur cette page, c’est que vous avez entendu parler de <strong>Smart Life</strong> ou que vous cherchez à dompter sa grande sœur, l’application <strong>Tuya Smart</strong>, pour installer une nouvelle ampoule Wi-Fi ou prise connectée, un interrupteur ZigBee acheté sur Amazon, un détecteur de mouvement déniché chez Action, un ventilateur connecté, un diffuseur d’huiles essentielles, une caméra de vidéosurveillance compatible Alexa ou Google Home, ou encore un robot aspirateur voire même une tondeuse automatique (rayez les mentions inutiles)…</p>
						
						<br><center><img src="images/smartlife/smartlife/img 1.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Eh oui, <strong>Smart Life c’est un peu le couteau Suisse des objets connectés</strong>. Cette application disponible sous <strong><a href="https://apps.apple.com/fr/app/smart-life-smart-living/id1115101477" target="_blank" rel="noopener noreferrer">><font color="blue">iOS</font></a></strong> comme <strong><a href="https://play.google.com/store/apps/details?id=com.tuya.smartlife&hl=fr&gl=US" target="_blank" rel="noopener noreferrer">><font color="blue">Android</font></a></strong> peut en effet tout gérer, ou presque. Elle est même très souvent à préférer à celle proposée par le fabricant de votre objet connecté.</p>
						<br>
						<p>Pourquoi ? Car <strong>Smart Life et Tuya sont fréquemment mises à jour</strong>, au contraire des applications en marque blanche, mais aussi car <strong>cela vous évitera d’avoir trois applications ou plus</strong> pour piloter vos objets… Alors qu’elles sont toutes basées sur la même technologie : Tuya Smart.</p>
						<br>
						<p>Suivez le guide pour tout savoir sur <strong>Smart Life de Tuya</strong> et son utilisation avec <strong>Clara</strong>.</p>

						<blockquote>
							<font color="red">Les applications suivantes utilisent la plateforme Tuya : Avidsen Home, BlitzWolf, Calex Smart, Lidl Home, LSC Smart Connect, Proscenic Home, Konyks, SwitchBot, Teckin. Vous pouvez choisir de les utiliser, bien entendu, mais nous vous conseillons plutôt de rassembler tous vos appareils sur Smart Life. C’est celle qui offre le plus de compatibilités.</font>
						</blockquote>
						<p><u>Ce contenu a été mis à jour le 16/11/2021 :</u> changement des captures d’écran suite à la mise à jour Smart Life de novembre 2021.</p>
						
						<br>
						<h2><strong>Smart Life, à quoi ça sert ?</strong></h2>
						
						<p>La réponse est simple et complexe à la fois. <strong>Ça sert à tout !</strong></p>
						<br>
						<p>Quand vous achetez un <strong>objet connecté</strong>, il va bien souvent fonctionner grâce au <strong>Wi-Fi de votre routeur</strong> ou box Internet afin d’être pilotable depuis n’importe où. Il faut donc le lier à votre réseau domestique pour lui permettre de communiquer avec les serveurs qu’il utilise dans le cloud afin qu’il puisse se tenir à jour des nouveautés, mais aussi qu’il applique les actions que vous souhaitez.</p>
						
						<br><center><img src="images/smartlife/smartlife/img 2.png" width="50%" height="50%" alt=""/></center><br>
						
						<br>
						<p>L’application <strong>Tuya / Smart Life est donc une sorte de passerelle</strong> entre l’objet connecté et son serveur d’une part, mais aussi l’appareil que vous utilisez pour transmettre les ordres (smartphones mais aussi assistants vocaux).</p>

						<blockquote>
							<font color="green">Notez que cela signifie aussi que l’ensemble des données entrées dans l’application, ainsi que l’activité de vos objets connectés, vont transiter et seront stockées sur les serveurs Tuya Inc. Rassurez-vous, cependant, la société chinoise possède des serveurs chez AWS en Europe qui sont respectueux du <strong><a href="https://www.cnil.fr/fr/reglement-europeen-protection-donnees"
								target="_blank" rel="noopener noreferrer">><font color="blue">règlement général sur la protection des données (RGPD)</font></a></strong>. Nous vous conseillons donc vivement d’opter pour la région Europe afin de bénéficier d’une meilleure protection de vos données personnelles.</font>
						</blockquote>
						
						<p>Mais avant toute chose, il est nécessaire de <strong>créer un compte Smart Life</strong> via un e-mail et/ou un <strong>numéro de téléphone</strong>, puis un <strong>mot de passe</strong>, et d’activer l’ensemble via un code envoyé par l’application. Ceci fait, vous pouvez commencer à vous servir de cette <strong>application assez géniale</strong>.</p>

						<br><center><img src="images/smartlife/smartlife/img 3.png" width="25%" height="25%" alt=""/></center><br>
						
						<h2><strong>L’application Smart Life</strong></h2>
						
						<p>L’utilisation de <strong>Smart Life est assez simple</strong> et le mode opératoire est surtout rigoureusement identique dans la plupart des cas et pour l’immense majorité des applications Tuya.</p>
						<br>
						<p>Pour <strong>ajouter un appareil</strong> :</p>
						<br>
						<p>&rarr; <strong>Connectez-vous au réseau Wi-Fi</strong> que vous souhaitez utiliser (attention car la plupart du temps, il faut être seulement en 2,4 GHz et non en 5 GHz, bien que cela soit moins vrai aujourd’hui),</p>
						<br>
						<p>&rarr; <strong>Activez votre Bluetooth</strong> et éventuellement votre GPS pour des faire des scénarios basés sur votre localisation (geofencing, horaires solaires…),</p>
						<br>
						<p>&rarr; <strong>Appuyez sur le « + » en haut à droite</strong>,</p>
						<br>
						<p>&rarr; A partir de là, plusieurs possibilités s’ouvrent à vous, à vous de <strong>sélectionner le bon type d’objet connecté</strong> !</p>
						<blockquote>
							<font color="red">Attention, votre smartphone doit parfois être connecté à la bande 2,4 GHz de votre Wi-Fi domestique et non en 5 GHz ! Enfin, c’était surtout vrai avant…</font>
						</blockquote>
						
						<p>Les produits à installer sont classés par types et ce sera à vous de venir choisir celui qui représente le mieux votre nouvelle acquisition. Par exemple, un interrupteur pour volet roulant sera considéré comme un interrupteur. Souvent, les guides accompagnant les appareils vous donnent l’information. On y trouve tout, du destructeur de moustiques à la climatisation, en passant par les aspirateurs robots ou encore les  passerelles ZigBee.</p>

						<br><center><img src="images/smartlife/smartlife/img 4.png" width="75%" height="75%" alt=""/></center><br>
						
						<p>Une fois que vous avez trouvé votre bonheur, <strong>sélectionnez l’appareil</strong> et suivez les instructions qui sont peu ou pour toujours les mêmes. Il faut <strong>mettre votre appareil en mode appairage</strong> et cela dépend du type d’objet :</p>
						<br>
						<p>&rarr; <strong>pour installer une prise connectée</strong> : il suffit d’appuyer quelques secondes sur le bouton physique</p>
						<br>
						<p>&rarr; <strong>pour installer une ampoule (voir tutoriel)</strong> : il faut l’allumer et l’éteindre plusieurs fois</p>
						<br>
						<p>&rarr; <strong>pour un installer un interrupteur pour volets roulants ou d’éclairage</strong> : il faut laisser appuyer sur l’une des zones capacitives quelques secondes, … Cela est en général expliqué dans la documentation.</p>
						<br>
						<p>Quand l’appareil entre en <strong>mode appairage</strong>, une lumière va clignoter rapidement. Vous pouvez donc ensuite passer à l’étape suivante. Cela consister à entrer <strong>votre nom de réseau (SSID)</strong>, si c’est la première fois que vous vous y connectez. L’application va rechercher l’appareil, s’y connecter, l’enregistrer sur serveur et l’initialiser pour utilisation</p>
						<blockquote>
							<font color="red">Parfois, cette étape ne va pas jusqu’à son terme. Les causes les plus probables sont un réseau qui n’est pas en 2,4 GHz, une erreur dans la clé Wi-Fi et, plus rarement, un problème de serveur. Si c’est un problème de serveur, il est nécessaire de prendre son mal en patience et d’attendre avant de recommencer quelques heures plus tard.</font>
						</blockquote>


						<p><strong>Une fois cela fait, votre appareil apparait avec son nom générique</strong>. Libre à vous de lui donner un patronyme à votre convenance en appuyant sur le petit crayon.</p>

						<br><center><img src="images/smartlife/smartlife/img 5.png" width="25%" height="25%" alt=""/></center><br>
						
						<p>Alors ? C’est facile non ? Attention, dans quelques cas, vous pouvez avoir une double ou une triple utilisation d’un appareil. Par exemple, une multiprise, une prise avec lampe… Dans ce cas, <strong>vous pourrez renommer chaque élément individuellement</strong> en entrant dedans depuis le dessin du produit.</p>

						<br><center><img src="images/smartlife/smartlife/img 6.png" width="25%" height="25%" alt=""/></center><br>
						
						<blockquote>
							<font color="green">Choisissez bien les noms, car quand vous les utiliserez avec Alexa, ce sont ces noms qu’ils faudra utiliser. Évitez aussi les noms trop proches ou avec des chiffres qui vont générer des conflits.</font>
						</blockquote>

						<h2><strong>Les scénarios</strong></h2>
						
						<p>L’un des points forts de <strong>Smart Life</strong> est de pouvoir <strong>adapter l’utilisation de vos appareils connectés à vos envies</strong>. Cette personnalisation peut être assez basique, comme définir des <strong>horaires fixes</strong> d’allumage ou de mise hors tension d’un élément. Pour cela, pas besoin d’aller dans la partie scénario de Smart Life, il vous suffit de <strong>cliquer sur l’horloge</strong> et de choisir les heures et les jours. Il est aussi possible de faire un compte à rebours.</p>
						
						<br><center><img src="images/smartlife/smartlife/img 7.png" width="70%" height="70%" alt=""/></center><br>

						<p><strong>Les scénarios et les automatisations</strong> permettent de faire des « programmations domotiques » plus poussées comme activer un élément lors de conditions particulières (détection sur un appareil, lever du soleil, coucher du soleil…).</p>
						<br>
						<p>Dans Smart Life, <strong>cliquez sur « Scénario »</strong> en bas de l’écran et <strong>« + »</strong> puis <strong>« Scénario »</strong>.</p>
						<br>
						<p>Entrez le nom de votre scénario et ensuite, en allant sur <strong>« ajouter une action d’exécution »</strong>, sélectionnez votre appareil et dans la partie <strong>« statut »</strong> choisissez ce qu’il faut faire.</p>
						<br>
						<p>Dans le cas ci-dessous, nous avons fait un scénario pour stopper des volets roulants en cours de descente. Une action très pratique !</p>
						
						<br><center><img src="images/smartlife/smartlife/img 8.png" width="100%" height="100%" alt=""/></center><br>

						<p>La fonction <strong>automatisation</strong>strong> permet la réalisation d’une action dans certaines circonstances, comme allumer des lumières quand le soleil se couche, ou encore activer un appareil suite à une détection. Même façon de faire, <strong>sélectionnez automatisation</strong> puis le <strong>« + »</strong>.</p>
						<br>
						<p><u><strong>Il y a trois zones :</strong></u></p>
						<br>
						<p>&rarr; <strong>les conditions</strong> (température, humidité, temps, lever / coucher du soleil, heure et appareil (comme un détecteur)</p>
						<br>
						<p>&rarr; <strong>les actions à exécuter</strong> (un scénario ou une automatisation qui existe déjà, une notification à recevoir, un retard dans une exécution, un appareil à piloter…)</p>
						<br>
						<p>&rarr; <strong>la plage horaire</strong> d’utilisation.</p>
						
						<br><center><img src="images/smartlife/smartlife/img 9.png" width="100%" height="100%" alt=""/></center><br>
						
						<p>Par exemple, vous voyez sur l’image suivante une <strong>automatisation pour allumer un bandeau LED quand le temps est pluvieux</strong>, ou encore <strong>tous les jours à 18 heures</strong>.</p>
						<br>
						<p>Vous pouvez tout aussi bien utiliser les données officielles et choisir <strong>« quand le soleil se couche »</strong>, d’où l’importance de bien renseigner vos informations personnelles et d’activer votre GPS pour que cela se fasse au bon moment et ainsi éviter que la lumière ne s’allume à 3h du matin ou que les volets ne s’ouvrent à minuit.</p>


						<br><center><img src="images/smartlife/smartlife/img 10.png" width="50%" height="50%" alt=""/></center><br>
						
						<h2><strong>Smart Life et Clara</strong></h2>
						<br>
						<p>Le <strong>pilotage à la voix avec Clara</strong> se fait très simplement. Il est bien entendu nécessaire d’avoir le plugin <strong><a href="stores.php"><font color="blue">SmartLife</font></a></strong> depuis le store de Clara</p>
						<br>
						<p>La première fois, il faut que vous saisissiez vos <strong>identifiants et mot de passe</strong>. Dans le fichier .js de smartlife pour l'activation ensuite la <strong>recherche des appareils connectés</strong> débute de façon automatique.</p>
						<br>
						<p>Dès que vous installerez un nouvel objet avec Smart Life, <strong>il remontera directement dans Clara</strong>, sans que vous n’ayez à faire quoi que ce soit. Génial, n’est-ce pas?</p>
						<br>
						<p>Il en va de même pour les scénarios qui vont faire leur apparition comme par magie et seront dès lors utilisables à la voix.</p>
						<br>
						<blockquote>
							<font color="green">Choisissez bien leur nom d’activation dans une routine, que Clara comprenne bien votre requête (attention à l’orthographe). Pour mieux organiser votre routines de Clara, donnez-leur des noms, vous les retrouverez ensuite par ordre alphabétique.</font>
						</blockquote>
						<br>
						<p>Smart Life n’a maintenant plus de secret pour vous. Si vous voulez aller un peu plus loin sur certains aspects, allez faire un tour du côté de nos tutos dédiés (groupes, routines, interrupteurs…). Oui on sait, on est trop sympas !</p>
