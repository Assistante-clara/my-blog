						<h1>Test LSC Smart Filament : une ampoule rétro tout à fait réglo</h1>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-user"></i> Admin</span> <span><i class="fa fa-clock-o"></i> 25 Aou 2019</span> <span><i class="fa fa-comment"></i> 4 comments</span> <span><i class="fa fa-folder"></i> Smartlife</span></p>
						</div>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-clock-o"></i> Mise à jour : 30 Sep 2021</span></p>
						</div>

						<p>Toute bonne installation de maison connectée commence par des ampoules connectées. Ça, tout le monde le sait. Et des tests, nous en avons fait : des blanches, des grises, des bonnes, des moyennes… Mais, parfois, nous voulons une ampoule connectée juste car elle est jolie, car elle sort de l’ordinaire. Et nous avons réussi à vous en trouver une, en donnant de notre personne. Nous avons pris notre petit chariot et sommes allés chez Action faire le plein de provisions. Les magasins de cette chaîne avaient proposé la prise connectée Flamingo dont nous avions fait le test il y a près d’un an . On va dire qu’<u>elle ne restera pas dans les annales</u>. Mais qu’en est-il de cette nouvelle gamme assez étoffée ?</p>
						<br>
						<p>En effet, Action propose en ce moment une gamme complète d’ampoule en E14 et E27, un strip light, une prise, un détecteur d’ouverture, une alarme… Cela nous donne bien des idées et la tentation est grande d’avoir tous ces produits à portée de main, en libre-service. C’est un gros point fort, sans parler des prix… Ils sont <strong>très compétitifs</strong>. Mais qu’avons-nous réellement pour ce montant? Nous allons voir cela avec notre choix (oui, oui, nous sommes un peu dispersés) : l’ampoule intelligente à filament E27.</p>
						<br>
						<p>C’est une ampoule de <strong>5,5 W pour 470 lm</strong> avec un <strong>design vintage très réussi</strong>. Il en existe plusieurs dans la gamme filament allant de la petite E14 à l’énorme E27, mais toujours en 470 lumens et avec un <strong>prix compris entre 8,95€ et 9,95€</strong>.</p>
						<br>
						<blockquote>
							<font color="green">Le test sur cette référence particulière vaut pour l’ensemble de la gamme filament, la technologie étant la même.</font>
						</blockquote>
						<br>

						<h2><strong>LSC Smart Filament : unboxing de l’ampoule vintage mais connectée</strong></h2>
						
						<p>Cette marque distributeur d’Action ne se trouve que dans certains de ses magasins et ne peut malheureusement pas être commandée sur le web. Cependant, là, pas de délais de livraison dignes d’un accouchement. Comme dit en préambule, le test de cette référence vaut pour la gamme <strong>LSC Smart Filament</strong> complète.</p>
						<br>
						<p>Notre <strong>ampoule E27</strong> (Edison 27 mm) est présentée dans un carton très imagé, glacé, coloré… Il fait très moderne. Il est en fait constitué de deux parties : un couvercle qui se retire assez facilement car tenu avec deux pastilles adhésives et un fond servant de contenant. <strong>Les informations fournies sont claires : puissance, fonctionnement avec l’application maison LSC App, compatible avec Clara et diverses données</strong>. Il est assez souple mais à l’intérieur, l’ampoule est bien calée entre deux cartons. Aucun risque de casse. <strong>La première impression est très bonne</strong>.</p>
						
						<br><center><img src="images/smartlife/smartlife 3/img 1.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Nous trouvons bien calé notre ampoule et au fond, une notice en 10 langues sur le mode de connexion. La première impression est « Waouh !!! ». Et pourtant, le loup, on l’a vu, et plusieurs fois, donc pour nous faire lâcher ce type d’interjection béate, il en faut. <strong>Elle est franchement réussie : son bulbe a une jolie couleur dorée, le culot est aussi dans la même gamme de nuance et ses 4 filaments sont assez réussit</strong>. Alors oui, attention, on parle de filaments mais ce ne sont pas des ampoules à incandescence. Ce sont des filaments constitués de très petites puces LED. Son aspect rétro est lié à cette aspect filament, cette <strong>couleur mordorée</strong> et cette <strong>forme d’ampoule à incandescence</strong> avec une petite proéminence sur le dessus.</p>
						
						<br><center><img src="images/smartlife/smartlife 3/img 2.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Au niveau matériaux, nous sommes sur une ampoule « comme avant » : bulbe en verre et culot en métal. Aucun système Wi-Fi apparent, pas de gros cache crado qui fait la moitié de l’ampoule comme sur les LED standard… Elle est <strong>très bien finie et très légère</strong> (moins de 50 g). Nous avons tous le souvenir de ces bulbes incandescents qui nous brulaient au 3ème degré si on avait le malheur de les toucher après les avoir éteints. Eh bien, ce n’est pas le cas, elle ne chauffe pas du tout. C’est un très bon point. La chaleur de ses blancs va varier <strong>1800 à 6500 K</strong>.</p>
						<br>
						<p>Au niveau des <strong>caractéristiques</strong>, elle fait <strong>6,5 cm de diamètre au plus large</strong> et <strong>11,5 cm de long</strong> une fois mise en place. Elle est de <strong>classe A+ (6 kWh/1000 h)</strong> et est annoncée pour <strong>15000 h de fonctionnement</strong> (1,7 an allumée en permanence et plus de 20 ans si utilisée 2 heures par jour). Elle fonctionne en <strong>Wi-Fi en 2,4 GHz, IEEE 802.11 b/g/n</strong>. Au niveau électrique, <strong>220-250 V</strong> et 50/60 Hz. Seule l’intensité lumineuse peut décevoir : <strong>470 lm</strong>. Il faut donc lui réserver une utilisation de lampe d’appoint, de lampe de chevet ou de décoration.</p>
						<br>

						<h2><strong>LSC Smart Filament : fonctionnement avec l’application mai… Smart Life !</strong></h2>
						
						<p>Les produits de la marque ont droit à leur <strong>application maison</strong>, <strong>LSC</strong>. Mais cette application est sur base Tuya, nous avons décidé de ne pas l’utiliser mais de passer directement par <strong>Smart Life</strong> afin d’éviter d’avoir une énième application à gérer. Ce qui tombe bien, car <strong>il n’y a pas de plugin LSC à l’heure actuelle</strong>. Si vous n’êtes pas habitué à utiliser Smart Life, allez voir nos différents <a href="smartlife1.php">tutoriels pour installer une ampoule</a>, mais c’est très simple.</p>
						<br>
						<p>Une fois dans l’application, appuyez sur le <strong>« + »</strong> en haut à droite et sélectionnez <strong>« éclairage »</strong> et <strong>« dispositifs d’éclairage »</strong>. Pour faire entrer la prise en mode appairage, <strong>allumez et éteignez là 3 fois de suite</strong> jusqu’à celle clignote (mais dans notre cas, elle s’est mise en mode appairage dès la mise sous tension) … Suivez ensuite la procédure normale : sélection du réseau Wi-Fi, laissez la détection se faire et renommez là. <strong>Il faut compter 30 secondes maximum</strong>.</p>

						<br><center><img src="images/smartlife/smartlife 3/img 3.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Pour vous en servir de façon simple, renommez votre ampoule et attendez que la mise à jour se fasse. C’est aussi très rapide. Voilà, votre ampoule connectée E27 est prête à être utilisée. Si vous avez le plugin SmartLife, Clara la découvre automatiquement.</p>
						<br>
						<p>Maintenant que l’ampoule est installée sur l’application, regardons de plus près ce que nous pouvons faire avec. L’interface est identique à toutes les autres ampoules. Vous pouvez l’allumer et l’éteindre en cliquant sur le logo ampoule. En allant dans « Schéma », vous aurez accès à une planification de l’allumage ou de la mise hors tension. En allant sur « Blanc », vous pourrez faire varier <strong>l’intensité lumineuse de 1 à 100%</strong> (contrairement aux 10 à 100% annoncé par l’application maison) et <strong>la chaleur des blancs de 1800 à 6500 K</strong> soit de l’extra chaud à la lumière du jour. Et bien entendu, il y a la possibilité de scénario, de routines via Clara… Via les plugins Clara, vous êtes limité aux couleurs de l’application (une quinzaine) qu’il sera facile d’appeler à la voix.</p>
						
						<br><center><img src="images/smartlife/smartlife 3/img 4.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Vous pouvez voir notre ampoule Action en action via ce test vidéo. Elle est très réactive, les couleurs sont très franches et les chaleurs bien marquées. La faible intensité lumineuse est contrebalancée par une luminosité profonde, irradiante. C’est <strong>un très bon produit… et très joli</strong>. Il n’y a aucun papillonnement de l’ampoule quand elle est filmée, preuve de la qualité du produit… Nous sommes réellement sur un très bon produit à un prix défiant toute concurrence.</p>

						<br><center><img src="videos/smartlife/smartlife 3/vid 1.mp4" width="50%" height="50%" alt=""/></center><br>
						
						<blockquote>
							<font color="green">En conclusion, notre Smart Filament LED est une excellente surprise. Elle interpelle déjà par son design retro très réussi et elle surprend ensuite par sa qualité de fabrication, ses blancs francs et profonds. Seule sa faible intensité lumineuse à 470 lm est à mettre dans les points faibles, mais ce n’est clairement pas une lampe principale. Et surtout, quel prix ! 8,95€ !!! C’est un rapport qualité prix imbattable !</font>
						</blockquote> 

