						<h1>LSC Smart Connect Security System : un ensemble accessible et efficace mais ne faisant pas de miracle</h1>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-user"></i> Admin</span> <span><i class="fa fa-clock-o"></i> 30 Aou 2019</span> <span><i class="fa fa-comment"></i> 4 comments</span> <span><i class="fa fa-folder"></i> Smartlife</span></p>
						</div>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-clock-o"></i> Mise à jour : 30 Sep 2021</span></p>
						</div>

						<p>Le dernier test des produits de la marque LSC Smart Connect nous a vraiment bluffé.<strong> Leurs ampoules connectes à filament sont vraiment très bonnes</strong>. Nous avons donc voulu tester une autre partie de leur gamme et en particulier l’ensemble des appareils connectés pouvant s’apparenter à un système de sécurité. Nous insistons bien sur la notion de « pouvant s’apparenter » <strong>car cela ne vaut pas un vrai système avec alarme</strong>, surveillance tierce et autre. Mais il en faut pour tout le monde. Nous ne vous proposons pas un, ni deux, mais trois tests en un !!! </p>
						<br>
						<p>En effet, Action propose en ce moment 3 produits LSC Smart Connect pouvant se combiner les uns les autres pour mettre en place à moindre coût un système fait maison : un capteur d’ouverture, un détecteur de mouvement et une sirène intelligente ! Chaque élément est <strong>disponible au prix de 9,95€</strong> ce qui en fait <strong>les moins chers du marché</strong>. Maintenant, il est nécessaire de voir ce que ça donne… un par un s’il vous plait !</p>
						<br>

						<h2><strong>Smart Door Sensor: compact mais efficace</strong></h2>
						
						<p>Notre Smart Sensor est présenté dans le <strong>packaging qualitatif</strong> de la marque <strong>LSC Smart Connect</strong>. Il n’y a vraiment rien à redire dessus. On retrouve le couvercle avec l’illustration du produit, le fait qu’il <strong>fonctionne sous Tuya, compatible avec le plugin smartlife de Clara</strong> et qu’il peut aussi permettre des scénario avec la sirène, des ampoules… Le fond va présenter ses caractéristiques techniques.</p>
						
						<br><center><img src="images/smartlife/smartlife 2/img 1.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>L’unboxing nous permet de découvrir : le corps du <strong>détecteur d’ouverture</strong>, le <strong>module additionnel</strong> et un <strong>jeu de deux piles</strong> (délicate attention, merci). Nous sommes tout d’abord surpris de la taille du détecteur. Il est vraiment compact : <strong>8 x 3 cm pour le module principal et 5 x 1,3 cm pour le secondaire, 1,5 cm d’épaisseur</strong>. En comparaison, un Zemismart fait 9,5 x 4 cm et 9,5 x 1,5 cm alors que Konyks affiche des dimensions un peu plus petite que le LSC (7,5 x 2,5 cm). Il est « logoté » LSC en gros et en gris. Bien ? Pas bien ? A vous de voir mais quelque chose de plus discret ne serait pas plus mal. Cela dit, sa taille réduite est très appréciable car avoir un parpaing à la place d’un détecteur d’ouverture est assez gênant…</p>
						<br>

						<p>Il est fait en <strong>plastique blanc mat</strong> d’assez bonne qualité. Pas de défaut constaté sur notre produit. Chaque module a du double-face assez efficace. Le capot arrière de l’élément principal se retire en appuyant sur le bouton du haut. Les piles s’insèrent sans problème et le bouton de reset / appairage est très accessible comme vous pouvez le voir. Le petit trou sur la face avant, en bas, est en fait une DEL rouge pour le statut de connexion.</p>

						<br><center><img src="images/smartlife/smartlife 2/img 2.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>L’appairage se fait avec Smart Life (<strong>et non LSC app car il n’y a pas de plugin</strong>) en <strong>Wi-Fi en 2,4 GHz</strong>, IEEE 802.11 b/g/n. Choisissez « Capteur de protection » et « Capteur de porte » dans la partie ajout de Smart Life. L’appairage se fait très rapidement en appuyant sur le bouton, laissez clignoter rapidement, sélectionnez votre réseau et hop. Changez le nom, faites des tests d’ouverture et de fermeture afin de voir s’il communique bien et c’est parti ! Vous pouvez ensuite le faire remonter sous Clara avec le plugin SmartLife (regardez les autres tests et tuto pour plus d’infos).</p>

						<br><center><img src="images/smartlife/smartlife 2/img 3.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Lors de ces tests, nous avons été assez surpris de la distance de détection. Comptez-environ 1 cm en écartement horizontal et facilement 1,5 à 2 cm en glissement. Cela permet de mettre le capteur dans la position adéquate, contrairement à d’autres qu’il faut régler au poil près. L’aimant utilisé est donc de bonne qualité, mais attention, si la porte est à la limite de la fermeture, il risque de ne pas vous le dire (d’où un placement adéquat). Regardez sur ces photos, nous sommes encore en zone de détection de fermeture !</p>
						
						<br><center><img src="images/smartlife/smartlife 2/img 4.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>A l’utilisation, pas grand-chose à redire : il se met facilement en place, capte assez bien les ouvertures et fermetures même s’il semble parfois réagir avec un peu de retard. Vous avez <strong>la possibilité de recevoir des notifications sur votre smartphone ou via Clara</strong> si vous faites une routine. L’application vous permet de visualiser le statu en direct : ouvert ou fermé, ainsi que l’historique des ouvertures et fermetures. Les paramètres vous permettent d’activer ou non les notifications.</p>

						<br><center><img src="images/smartlife/smartlife 2/img 5.png" width="75%" height="75%" alt=""/></center><br>
						
						<blockquote>
							<font color="green">Pour conclure sur ce détecteur d’ouverture, nous sommes sur un super rapport qualité/prix : il est compact, avec une zone large de détection, facile à installer et appairer et fonctionne très bien. Et cela pour 9,95€ ! N’hésitez pas une seconde !</font>
						</blockquote>

						<h2><strong>Smart Movment Sensor : ça bouge… ça détecte… c’est bien</strong></h2>
						
						<p>Notre détecteur de mouvement est présenté exactement de la façon que les autres produits de la gamme, et en particulier le détecteur d’ouverture. On va donc pas trop d’appesantir dessus. On y découvre : le détecteur de mouvement, un support et un jeu de deux piles assez balaises (des CR123/A). Le sensor est une rondelle de 6 cm de diamètre (soir un rayon de 3 cm et une circonférence de 18,84 cm et une surface de … ok, mais c’est bientôt la rentrée aussi) et une épaisseur au maximum du dôme de 4 cm. Il est donc dans la <strong>taille moyenne du marché.</strong></p>
						
						<br><center><img src="images/smartlife/smartlife 2/img 6.png" width="70%" height="70%" alt=""/></center><br>

						<p>Niveau matériaux, il est fait du même <strong>plastique blanc</strong> que les autres pour le corps et d’un autre plastique opaque translucide avec des motifs d’alvéoles de balle de golf. On y retrouve le <strong>petit logo LSC bien plus discret que le placard mis sur le détecteur d’ouverture</strong>. L’arrière a une petite plaque métallique qui va permettre de tenir <strong>par un aimant</strong> sur le support livré. Le support est lui aussi en plastique épais et assez bien conçu : il a un double-face mais si vous le mettez sur une surface métallique, il va aussi tenir par l’aimantation du module. <strong>Le module ne va pas simplement venir s’y poser mais s’y insérer afin de bien tenir</strong>.</p>
						
						<br><center><img src="images/smartlife/smartlife 2/img 7.png" width="100%" height="100%" alt=""/></center><br>

						<p>Le capot arrière du détecteur se retire en le faisant tourner. Comme pour le détecteur d’ouverture, les piles s’insèrent sans problème et le bouton de reset / appairage est très accessible comme vous pouvez le voir. C’est le module qui va luire un peu pour le mode appairage et non une LED.</p>
						<br>
						<p>Nous n’allons pas vous refaire l’affront de vous dire comment appairer avec Smart Life car là, si vous n’avez pas encore compris avec la première explication, le seul moyen est qu’on se déplace à domicile… Choisissez simplement « capteur vocal / lumineux ».</p>
						
						<br><center><img src="images/smartlife/smartlife 2/img 8.png" width="100%" height="100%" alt=""/></center><br>
						
						<p>Comment cela fonctionne ? Où le mettre ? Nous sommes sur un capteur passif, donc avec un système infra rouge qui va juste vérifier si quelque chose devant le capteur n’a pas la même signature thermique que l’ambiance. Il fonctionne donc dans le noir et est donné pour <strong>une plage d’utilisation en 0 et 55°C</strong>. Vous pouvez donc le mettre dans votre entrée pour pouvoir ensuite activer une sirène grâce à un scénario ou une lumière grâce à une routine. Les possibilités sont vraiment variées.</p>
						<br>
						<p>Le début du test n’était pas vraiment concluant car s’il est dans une zone de passage très fréquent, voir permanent, il ne va pas s’activer régulièrement, voir une seule fois. Ce qui est assez logique sur le principe, le but étant de s’activer en cas de changement d’environnement. Dans notre cas, <strong>nous l’avons installé au fond d’une boîte à lettre</strong>. Dès qu’une enveloppe est glissée dans la fenêtre, il va s’activer et envoyer une notification sur le smartphone ou sur Clara si vous paramétrer une routine du genre « vous avez une lettre ». C’est en effet bien plus simple à régler qu’un détecteur d’ouverture de porte sur une fente de B.A.L.</p>
						<br>
						<p>L’application vous permet également de voir l’historique des détections, comme les autres capteurs.</p>

						<br><center><img src="images/smartlife/smartlife 2/img 9.png" width="75%" height="75%" alt=""/></center><br>
						
						<br>
						<blockquote>
							<font color="green">Que dire sur ce produit ? Là encore, très bonne surprise pour le rapport qualité / prix. Il fonctionne de façon tout à fait satisfaisante, il s’installe très facilement et les notifications arrivent assez rapidement (même s’il y a parfois des latences). Pour 9,95€, vous ne risquez pas grand-chose et pour le coup, c’est du tout bon.</font>
						</blockquote>
						
						<br>
						<h2><strong>Smart Siren : résisterons-nous à son chant ?</strong></h2>
						
						<p>Le dernier objet de la gamme est celui qui doit tous les réunir, il est rond, un anneau rouge, c’est… une sirène ! En effet, grâce à des routines et des scénario, à vous la mise en place d’un système fait maison pour vriller les tympans des envahisseurs ! Mais attention, soyons clairs. <strong>Si on compare un système d’alarme digne de ce nom à un Mastiff tibétain nourrit aux jeunes enfants et en jeûn depuis 1 semaine, alors notre sirène serait un Chihuahua obèse souffrant de cataracte venant de s’enfiler une gamelle</strong>… C’est mignon, ça couine un peu, mais c’est tout. Vous allez comprendre pourquoi…</p>

						<br><center><img src="images/smartlife/smartlife 2/img 10.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>La boite de notre sirène est un peu plus grosse que les deux autres mais est identique en tous points sur les autres aspects. Et qu’y a-t-il dans cette boite ? Notre sirène qui se présente sous <strong>la forme d’un rond 9,5 cm de diamètre (donc 4,75 cm de rayon soit… Ok, ok…) et d’une épaisseur de 7,5 cm avec la fiche ou 4 cm une fois branchée)</strong>. A défaut de ne pas être esthétique, elle n’est pas forcément disgracieuse. On y voit un anneau rouge qui va s’allumer pendant qu’elle chante ou pendant le mode appairage. Il y a des ouvertures pour le son sur le devant et sur les côtés. 3 boutons sont présents : un pour la mise sous tension et l’appairage et deux autres pour le volume (110 dB max, le seuil de la douleur étant à 120 dB). Elle est en plastique grisâtre de bonne qualité.</p>

						<br><center><img src="images/smartlife/smartlife 2/img 11.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Pour la faire fonctionner, <strong>il faut la brancher au secteur (100-240 V)</strong> via sa fiche dépourvue de mise à la terre. Il n’y a pas de système de piles ou autre. Le gros faible du produit est donc là. <strong>Pour l’éteindre en cas d’intrusion, on débranche et c’est tout</strong>. Il faudrait donc aller la brancher dans des combles, derrière ou sous un meuble afin de ne pas la rendre visible ou plus difficilement détectable. Mais selon les habitations et les intérieurs, cela ne sera pas possible. <u>Garder bien cela en tête si vous voulez l’acheter.</u></p>
						<br>
						<p><strong>L’appairage se fait très facilement</strong>. Vous branchez et ça se met automatiquement dans le mode adéquat (sinon, appuyez quelques secondes sur le bouton). <strong>Choisissez alarme sous Smart-Life et laissez-vous guider</strong>.</p>

						<br><center><img src="images/smartlife/smartlife 2/img 12.png" width="75%" height="75%" alt=""/></center><br>
						
						<p>L’application va vous permettre de voir l’<strong>historique des alarmes</strong> mais également, dans les paramètres, de <strong>choisir le volume sonore de l’alarme</strong>, le <strong>type d’alarme</strong> (sonore, visuel ou les deux), des <strong>durées de mise sous alarme</strong> ou de l’éteindre. <u>C’est plutôt bien fait et intuitif.</u> Elle va aussi venir intégrer directement un groupe alarme sous Smart Life.</p>

						<br><center><img src="images/smartlife/smartlife 2/img 13.png" width="75%" height="75%" alt=""/></center><br>
						
						<p>Vous pouvez maintenant faire un <strong>scénario pour mettre en marche votre alarme</strong> en cas d’ouverture d’une porte par exemple ou encore d’une détection de mouvement. Allez pour cela dans automatisation de l’onglet scenario. C’est très simple, regardez cet exemple. Et regardez-le en pleine action sur notre <strong>vidéo pleine de suspens !</strong></p>

						<br><center><img src="images/smartlife/smartlife 2/img 14.png" width="25%" height="25%" alt=""/></center><br>
						
						<blockquote>
							<font color="green">Alors au final, cette sirène ? Bien ? Autant être honnête, oui elle n’est pas mal, elle fait le boulot en se mettant à hurler dès que le scenario s’enclenche, mais ne pas mettre un système de batterie pour l’alimenter même si elle est débranchée lui fait franchement perdre de son intérêt. </font><font color="red">A part si vous avez la possibilité de mettre votre appareil dans un endroit inaccessible, passez votre chemin. C’est à peine dissuasif si vous tombez sur des malfrats aguerris.</font>
						</blockquote> 

						<br><center><img src="videos/smartlife/smartlife 2/vid 1.mp4" width="50%" height="50%" alt=""/></center><br>
						
						<blockquote>
							<font color="green">En conclusion, nous sommes sur des produits qui restent d’un très très bon rapport qualité prix car tous à 9,95€. Le détecteur d’ouverture est excellent, le détecteur de mouvement est très bien mais l’alarme est plus un gadget qu’autre chose. Même si pour 9,95€ elle peut faire prendre peur à un cambrioleur du dimanche, elle est peu dissuasive du fait de sa conception.
							Mais finalement, n’hésitez pas, foncez ! Il n’y pas d’équivalent à ce rapport qualité prix en ce moment.</font>
						</blockquote> 

