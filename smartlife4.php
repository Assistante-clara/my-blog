						<h1>Test Guirled : guirlande électrique compatible Tuya / Smart Life</h1>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-user"></i> Admin</span> <span><i class="fa fa-clock-o"></i> 3 Nov 2019</span> <span><i class="fa fa-comment"></i> 4 comments</span> <span><i class="fa fa-folder"></i> Smartlife</span></p>
						</div>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-clock-o"></i> Mise à jour : 15 Déc 2021</span></p>
						</div>

						<p>En cette période où l’on commence à penser aux préparatifs de fêtes de fin d’année : que va-t-on mettre dans les assiettes ou qui allons-nous inviter pour le soir du réveillon, on pense souvent en dernier au décor… alors Les Clariens pour vous aider à ne pas perdre de temps sur ce dernier sujet (on vous laisse vous occuper du reste hein) ont testé pour vous une guirlande <strong>Guirled</strong>, connectée bien sûr et compatible avec l'assistante vocal Clara évidemment grâce à l’application Smart Life sur laquelle elle vient s’appairer.</p>
						
						<br><center><img src="images/smartlife/smartlife 4/img 1.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Notre <strong>guirlande d’intérieur</strong> (elle n’est pas étanche) est un produit de <a href="https://www.guirled.com/" target="_blank" rel="noopener noreferrer"><strong><font color="blue">Guirled, une entreprise française créée en 2002</font></strong></a> qui assure la conception, l’assemblage et la distribution de ses produits. Pas mal hein? Elle vend ses produits soit sur Amazon en mode « guirlande toute prête » soit sur son site directement où il est possible de personnaliser sa guirlande. C’est ce 2e choix que nous avons fait. Nous avons composé notre guirlande selon la configuration que nous souhaitions : différentes possibilités sont proposées. Voyons si la qualité française y est.</p>
						<br>
						<h2><strong>Guirled : une guirlande tout en rondeur</strong></h2>
						
						<p>Nous ouvrons notre carton de livraison (assez grand d’ailleurs) pour découvrir notre commande. Celui-ci contient le coffret de notre guirlande et celui de la batterie externe que nous avons choisi de prendre pour nous affranchir des prises. Nous pourrons ainsi placer notre guirlande où bon nous semblera. Notre box rectangulaire toute lisse de couleur marron contenant l’ornement lumineux choisi (16 boules) est pourvue d’une découpe recouverte par un cellophane translucide épais nous offrant d'emblée une visualisation de ce que nous avons commandé. Tout de suite, nous avons le rendu sous les yeux.</p>
						<br>
						<p>Le carton est scellé avec un grand autocollant portant la mention : <strong>voice control</strong>, mention également portée sous la marque <strong>Guirled</strong> placée sur la partie haute de la boite. Enfin un tampon indiquant une <strong>garantie de 3 ans</strong> est apposé en bas à droite de la boite.</p>
						<br>
						<p>Retournons notre coffret pour voir ce que l’on va y trouver. Et bien il s’agit ni plus ni moins d’une méthodologie en 4 étapes. L’installation des ampoules chacune sur leur connecteur et le branchement avec la prise à fixer sur un des 2 ports USB . <strong>Bonne initiative ce double port USB</strong>, car on pourra ainsi en même temps faire fonctionner la guirlande en utilisant un port et recharger la batterie avec le deuxième port. Un troisième schéma montre le <strong>bouton ON/OFF</strong>.  Enfin, le quatrième dessin nous indique que cette guirlande fonctionnera en <strong>Wi-Fi sur le 2.4 GHz</strong> uniquement et avec l’application fétiche que Les Clariens ont affectueusement surnommée <a href="smartlife.php"><strong><font color="blue">le couteau suisse de la maison connectée : Smart Life.</font></strong></a></p>
						
						<br><center><img src="images/smartlife/smartlife 4/img 2.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Comme dit précédemment, nous avons aussi opté lors de notre commande pour une batterie externe, qui nous permetta de nous affranchir de prise. Celle-ci est livrée dans un petit cartonnage blanc lisse sur lequel la photo de la batterie est imprimée. Sur la face avant un visuel de la batterie est imprimé ainsi que sa capacité d’énergie stockée : <strong>6000 mAh</strong> soit comme nous l’indique également le petit logo à coté représenté par un chronomètre : <strong>70h d’utilisation possible à 25%</strong>. Au dessus, une pastille rose nous informe qu’il faudra <strong>5h de charge</strong> pour obtenir cette autonomie. Au verso de notre petite boite, nous retrouvons comme sur le grand coffret quatre schémas qui nous indiquent que l’on peut lui brancher la guirlande grâce à la prise USB qui la termine. Enfin, l’information indiquant que la batterie peut être rechargée avec le cordon fourni relié à la prise puisqu’elle comporte 2 ports USB</p>
						<br>
						<p>A l’arrière des boites, nous retrouvons les informations techniques : ampoules LED (gage d’une faible consommation), connectivité, ainsi que matériaux utilisés pour la structure extérieure des boules : du fil de coton. Enfin, une pastille représentant l’hexagone nous indique qu’une <strong>entreprise française</strong> (du sud et plus exactement près de Nîmes)  est à l’origine de ce produit. Cocorico !</p>
						
						<br><center><img src="images/smartlife/smartlife 4/img 3.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Maintenant que le tour du packaging est fait, passons à l’essentiel… Notre guirlande !</p>
						<br>
						<p>Nous sortons de sa grande boite les <strong>16 sphères</strong> qui sont bien calées dans le coffret compartimenté. Elles font respectivement <strong>6 cm de diamètre</strong> chacune et sont élaborées en <strong>polycarbonate translucide</strong> pour permettre aux LED de diffuser leur flux lumineux. Chacune est recouverte d’un coton tressé de la couleur que nous avons choisi lors de la composition de notre guirlande (le choix est vaste parmi une palette de 30 couleurs). Sur le dessous de la boule, nous avons un trou permettant d’y insérer la LED. <strong>Elle ne consomme que 1.6W</strong>.</p>
						<br>
						<p>Dans la partie droite du coffret, nous trouvons le bloc prise dans un sachet plastique translucide. Celui-ci comporte 2 ports USB et affiche le <strong>marquage CE</strong>, ainsi que les mentions <strong>« Input : 100-240V  AC – 50/60hz – 0.2A »</strong> et <strong>Output : 5V 2A</strong>. Notre guirlande à la <strong>certification CE, UL, FCC et RoHS</strong> (la norme UL et FCC concernant l’Amérique du Nord). Nous pouvons être tranquille et même voyager avec elle ! A signaler que celle-ci est toutefois Made in China, comme la plupart des produits électroniques.</p>
						<br>
						<p>Le câble de la guirlande, disposé dans la même alvéole, est d’une longueur totale de 3.20 m, <strong>1.60m avec les boules</strong>, avec des <strong>sphères espacées de 10 cm</strong> chacune. Sur celui-ci se trouve évidemment <strong>l’interrupteur ON/OFF</strong> qui nous permettra de procéder à l’appairage, mais nous y reviendrons en temps voulu. Nous trouvons également joints dans le coffret deux petits livrets, l’un proposant diverses présentations et produits disponibles et l’autre étant le manuel d’utilisation.</p>
						<br>
						<p>Continuons notre inventaire. Après le contenu du grand coffret, voyons ce que contient la plus petite des boites celle de la batterie : la batterie ! Et le cordon USB qui servira pour la recharger…. Logique imparable. A noter que sur la batterie est fixée une dragonne. Un petit plus appréciable qui permettra de la suspendre à une branche du sapin, par exemple, afin qu’elle ne traine pas au sol.</p>
						
						<br><center><img src="images/smartlife/smartlife 4/img 4.png" width="50%" height="50%" alt=""/></center><br>
						<br><center><img src="images/smartlife/smartlife 4/img 5.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Maintenant que nous avons détaillé notre guirlande, et avant de passer à son installation, voici la traditionnelle fiche technique.</p>
						<br>
						<p><strong>Longueur</strong> 3,2 mètres (16 boules), 4 mètres (24 boules)</p>
						<p><strong>Longueur entre la première et la dernière boules</strong> 1,6 mètre (16 boules) ou 2,4 mètres (24 boules)</p>
						<p><strong>Distance entre deux boules</strong> 10 centimètres</p>
						<p><strong>Consommation</strong> 1.6W (16 boules), 2.4W (24 boules)</p>
						<p><strong>Boules</strong> Plastique transparent (polycarbonate) entouré de coton tressé – Sans danger pour les enfants</p>
						<p><strong>Diamètre d’une boule</strong> 6 centimètres</p>
						<p><strong>Poids</strong> 421 g pour 16 boules (batterie incluse)</p>
						<p><strong>Adaptateur USB</strong> 2 ports USB 5V jusqu’à 2A max</p>
						<p><strong>Usages spécifiques</strong> usage intérieur uniquement</p>
						<p><strong>Certification</strong> CE, UL, FCC, RoHS</p>
						<p><strong>Garantie</strong> 3 ans partout dans le monde</p>

						<br><center><img src="videos/smartlife/smartlife 4/img 6" width="50%" height="50%" alt=""/></center><br>
						
						<h2><strong>Guirled : installation de la guirlande connectée</strong></h2>

						<p>Dans un premier temps, il faut installer les 16 sphères sur la guirlande. Rien de compliqué, il faut les clipser sur les supports prévus. Ensuite brancher le cordon sur un des 2 ports USB de la prise que l’on va raccorder au courant électrique. La procédure d’appairage va pouvoir commencer une fois que l’on aura appuyé <strong>5 secondes sur le ON/OFF</strong> du bouton tout translucide lui aussi. Une fois fait, <strong>la guirlande se met à clignoter rapidement</strong> et se met en <strong>mode configuration</strong>, prête pour être reconnue dans l’application Smart Life. Cette application, les Clariens la connaissent bien et c’est loin d’être le premier objet connecté que nous lui ajoutons. Si, pour vous, cette guirlande constitue votre première installation sous Smart Life, jetez un coup d’œil à notre tutoriel. En ce qui nous concerne, nous allons directement dans l’application sur <strong>« ajouter »</strong> puis <strong>« éclairage »</strong>. Après avoir renseigné les SSID et mot de passe de notre réseau <strong>Wi-Fi 2.4 Ghz</strong>,  notre guirlande est rapidement reconnue. Nous pouvons alors la renommer, comme le permet généralement Smart Life, et même changer d’icône via le crayon situé en haut à droite. Il sera ainsi plus aisé de la repérer sur la page d’accueil de l’application en cas de besoin.</p>
						<br><center><img src="images/smartlife/smartlife 4/img 7.png" width="50%" height="50%" alt=""/></center><br>
						<br><center><img src="images/smartlife/smartlife 4/img 8.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Lorsque l’on clique justement sur notre guirlande nous avons une ampoule qui apparait. Alors, c’est dommage, mais il s’agit d’une seule ampoule. Une seule ampoule qui dictera le comportement des 16 LED de notre guirlande. Nous n’aurons donc pas 16 ampoules configurables individuellement, mais la possibilité d’activer plus ou moins l’intensité de la lumière depuis cet icône, et même en cliquant en bas sur « schedule » de procéder à des programmations</p>
						
						<br><center><img src="images/smartlife/smartlife 4/img 9.png" width="50%" height="50%" alt=""/></center><br>
						
						<h2><strong>Et avec Clara ?</strong></h2>
						
						<p>Et bien pas de surprise, notre guirlande est reconnue comme une ampoule par Clara et toutes les possibilités seront offertes pour l’utiliser dans une routine. Il sera ainsi possible d’être accueilli le soir, en rentrant chez soi, par une routine nous plongeant dans l’atmosphère des fêtes de fin d’année (mais pas que).</p>
						<br>
						
						<blockquote>
							<font color="green">En somme, il s’agit d’un beau produit décoratif, élaboré en France, et compatible Alexa. Que demander de plus ?  La guirlande est disponible sur Amazon.fr dans des configurations toutes prêtes et en 24 boules au prix de 34.99€ (pas de format 16 boules proposé pour le voice control) auxquels il faut rajouter la batterie au tarif 15.99€ – ou sur le site : www.guirled.com si vous prenez la même configuration que nous en 16 boules et en mode « je crée »  (30.99€ + la batterie 14.99€ + 3.49€ de frais de livraison car notre commande est inférieure à 50€).</font>
						</blockquote> 

