						<p>Bon voilà, je vous fait une petite documentation sur le nouveau système <strong>multiroom de Clara</strong> qui a été plutôt trés bien conçue, car l'ancien plugin multiroom était trop fastidieux pour certaines personnes a utiliser et surtout de s’y retrouver tant les commandes et les modifications étaient nombreuses.</p>
						<br>
						<p>C’est pourquoi nous vous proposons cette nouvelle option intégrer directement dans le code source de <strong>Clara</strong>. Vous pouvez aussi vous rendre sur la rubrique installation de ce site, très complète, qui peut répondre à un grand nombre de questions.</p>
						<br>
						<p>N’hésitez pas, enfin, à vous orienter vers nos tutoriels « installation de Clara » en cas de besoin ou vers notre guide, plus connu sous le nom de GDNC, le Guide du Domptage Numérique de Clara.</p>
						<br>
						<p>Nous vous invitons également à poser toutes vos questions sur notre page Facebook, sur le groupe Facebook le plus convivial consacré à Clara, ou encore à nous faire part de vos découvertes sur le compte Twitter des Clariens.</p>
						<br>

						<h2><strong>Multiroom :</strong></h2>
						
						<p><strong>Le principe :</strong></p>		
						<p>Un simple pc là où vous venez d'intaller la nouvelle mise à jour de Clara va devenir le <strong>maitre</strong> et donc servir de serveur</p>
						<p>Maintenant il n'y a plus lancer le système Clara, à partir de maintenant les autres appareils deviendront des <strong>esclaves</strong> pc/windows/linux, tablettes, montres, smartphones, ou autres.............</p>
						<p>Et donc sur les autres appareils, on ouvrira une simple page web Google Chrome</p>
						<br>
						<p>Donc on récupère l'adresse ip du pc maitre (serveur) si celle-ci est 192.168.0.10</p>
						<p>Alors l'adresse de la page sera <strong>https://192.168.0.10:4300</strong></p>
						<br>
						<p>Et c'est tout !!! Le système multiroom est opérationnel</p>
						<br>
						<p>Votre client <strong>esclave</strong> communique dans les deux sens avec le <strong>maitre</strong></p>
						<br>
						<p>Pour un client pc il suffira alors de parler comme sur le maitre</p> 
						<p>Pour un client smartphone/tablette (ios) il faudra cliquer sur le micro, pour activer la reco ( les smartphones et tablettes étant trop sensible au bruit)</p>
