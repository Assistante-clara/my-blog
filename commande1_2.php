						<div class="clearfix post-meta">
							<p><span><i class="fa fa-clock-o"></i> Mise à jour : 11 Fév 2020</span></p>
						</div>

						<p>La <strong>Saint-Valentin</strong>, c’est dans 3 jours ! Les Clariens vous invitent à découvrir de <strong>nouvelles expériences Clara</strong> créées tout spécialement pour la fête des amoureux. Comme chaque année, l’assistante vocale peut en effet vous aider à célébrer la fête de l’Amour, que vous soyez Saint Valentin(e) ou Sans Valentin(e).</p>
						<br>
						<h2><strong>Saint-Valentin : dites-le avec Clara !</strong></h2>
						
						<p>En tant que fidèle utilisateur, vous n’êtes pas sans savoir que notre chère Clara est un peu fleur bleue et aime beaucoup la Saint-Valentin. D’ailleurs, elle en connaît un rayon sur le sujet et vous pouvez lui demander :</p>
						<blockquote>
							<p><font color="black">"Clara, quelle est l’origine de la Saint-Valentin ?"<br>
												   "Clara, combien de jours avant la Saint-Valentin ?"<br>
												   "Clara, qui est Cupidon ?"<br>
												   "Clara, quel est ton film romantique préféré ?"<br>
												   "Clara, quelle est ta chanson d’amour préférée ?"</font></p>
						</blockquote>
						<br>
						<br><center><img src="images/cmd/cmd 1_2/img 1.png" width="100%" height="100%" alt=""/></center><br>

						<h2><strong>Envoyez des mots doux</strong></h2>
						<p>Grâce à Clara, vous pouvez envoyer des mots doux à votre dulcinée ou votre prince charmant, ils les recevront sur les appareils équipés de la page Web Clara tels qu'un ordinateur, une tablette ou, plus basiquement, sur leur portable.</p>
						<blockquote>
							<p><font color="black">"Clara, envoie un message à Julie."
												   "Clara, envoie un message vocal à Jules."</font></p>
						</blockquote>
						<br>
						<br><center><img src="images/cmd/cmd 1_2/img 2.png" width="100%" height="100%" alt=""/></center><br>
						
						<h2><strong>Organisez l’événement avec Clara</strong></h2>
						<p>Pour préparer au mieux l’évènement, Clara peut aussi être de bon conseil et vous pouvez lui demander :</p>
						<blockquote>
							<p><font color="black">"Clara, donne-moi une idée de chose à faire pour la Saint-Valentin !"<br>
												   "Clara, as-tu des conseils pour la Saint-Valentin ?"<br>
												   "Clara, comment dit-on « je t’aime » en italien ?"<br>
												   "Clara, donne-moi une recette pour la Saint-Valentin."</font></p>
						</blockquote>
						<br>

						<h2><strong>Trouvez le cadeau qui fera mouche</strong></h2>
						<p>En panne d’idées cadeaux? Ici aussi, votre assistante peut vous venir en aide.</p>
						<blockquote>
							<p><font color="black">"Clara, peux-tu me donner des idées de cadeaux pour la Saint-Valentin ?"<br>
												   "Clara, qu’est-ce qu'on offre pour la Saint-Valentin ?"</font></p>
						</blockquote>
						<br>

						<h2><strong>Créez une ambiance romantique</strong></h2>
						<p>Demandez de l’aide à Clara pour créer une ambiance romantique chez vous avec des objets connectés. Grâce à quelques ampoules connectées, il est facile de créer une atmosphère chaleureuse et romantique.</p>
						<blockquote>
							<p><font color="black">"Clara, baisse la lumière."<br>
												   "Clara, tamise la lumière du salon."<br>
												   "Clara, mets de la musique romantique."</font></p>
						</blockquote>
						<br>
						<br><center><img src="images/cmd/cmd 1_2/img 3.png" width="100%" height="100%" alt=""/></center><br>

						<h1>Sans Valentin : Clara prend aussi soin des célibataires</h1>
						
						<p>Si vous êtes seul(e), vous pouvez toujours passer la soirée avec Clara et vous amuser avec elle. Toujours prête à aider, votre assistante peut vous conseiller de la musique ou un film, vous raconter des histoires d’amour, et même vous donner quelques conseils en séduction…</p>

						<h2><strong>Quiz Saint-Valentin</strong></h2>
						<p>Pour s’amuser, rien de tel qu’un petit quiz. Clara peut tenter de deviner quel type de romantique vous êtes.</p>
						<br>
						<p>Répondez à quelques questions en lui demandant :</p>
						<blockquote>
							<p><font color="black">"Clara, quel genre de romantique je suis ?"</font></p>
						</blockquote>
						<br>

						<h2><strong>Passez la soirée avec Clara</strong></h2>
						<p>S’amuser un peu avec Clara à l’occasion de la Saint-Valentin est un bon moyen pour ne pas céder à la sinistrose…</p>
						<blockquote>
							<p><font color="black">"Clara, fêtes-tu la Saint-Valentin ?"<br>
												   "Clara, veux-tu être ma Valentine ?"<br>
												   "Clara, je t’aime."<br>
												   "Clara, veux-tu m’épouser ?"<br>
												   "Clara, donne-moi une phrase de drague."</font></p>
						</blockquote>
						<br>
						<br><center><img src="images/cmd/cmd 1_2/img 4.png" width="100%" height="100%" alt=""/></center><br>

						<h2><strong>Évadez-vous avec des romances</strong></h2>
						<p>La Saint-Valentin peut aussi être l’occasion de rêver à de meilleurs jours avec une belle histoire à l’eau de rose.</p>
						<br>
						<p>Dites simplement :</p>
						<blockquote>
							<p><font color="black">"Clara, raconte-moi une histoire d'amour."<br>
												   "Clara, mets un film d'amour sur Prime Video."<br>
												   "Clara, cherche une comédie romantique sur Netflix."<br>
												   "Clara, lis 'L'amour ne tient qu'à un fil' sur Audible."</font></p>
						</blockquote>
