						<h2 id="section_further_down_3"><strong>Le détecteur de mouvement pour éteindre les lumières quand il n’y a plus personne dans la pièce.</strong></h2>
						
						<p>C’est un autre geste du quotidien que nous, ou plus particulièrement les enfants, ont tendance à oublier. Couper la lumière quand on sort d’une pièce. C’est pourtant simple, mais pas toujours appliqué ou pas directement. Pourtant, quand vous quittez la pièce, si personne d’autre n’est dans la pièce, vous gaspillez tout simplement de l’électricité inutilement.</p>
						<br>
						<p><strong>Les détecteurs de mouvements</strong>, associés à l’éclairage permet de savoir quand il n’y a plus personne dans une pièce et éteindre la lumière en conséquence.</p>
						<br>
						<br>
						
						<h2 id="section_further_down_4"><strong>Exemple de scénario domotique pour gérer la lumière automatiquement.</strong></h2>
						
						<br><center><img src="images/archives/img.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Autre avantage, puisque celui-ci se chargera bien évidement également de l’allumage de la lumière, mais peu aussi dans le cas où la luminosité est inférieure à un certain seuil. Une manière supplémentaire de gérer de manière intelligente la lumière dans les pièces de passage.</p>

						<br><center><img src="images/archives/img.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Cela peut se faire via un système domotique et un scénario comme avec le système Eedomus, mais également avec une solution <strong>maison connectée</strong> de type Philips Hue, Ikea ou encore Lidl qui propose depuis la semaine dernière un <strong>détecteur de mouvement</strong> économique et performant.</p>
						<br>
						<p>Les détecteurs sont nombreux en fonction de votre <strong>système domotique</strong> ou de votre <strong>solution maison connectée</strong> en oeuvre dans la maison. Vous avez sans doute déjà ce type de détecteur chez vous si vous avez déployé de la domotique. Si toutefois vous cherchez une référence performante, sachez que le détecteur Zigbee Aqara est un des plus performants dans la gamme économique. Sinon, le dernier né de chez Lidl est aussi un capteur à prendre en considération tout comme celui de Ikea Tradfri.</p>
						<br>
						<br>

						<h2 id="section_further_down_5"><strong>Le micromodule pour gérer les volets au rythme du soleil et profiter de la chaleur naturelle.</strong></h2>
						
						<p>La <strong>gestion automatique des volets</strong> peut aussi aider dans cette démarche économique. En effet, le simple fait d’ouvrir les volets pour laisser entrer le soleil et ses pouvoirs de chauffage naturel au travers de la vitre, mais également <strong>la maîtrise de la fermeture</strong> au bon moment le soir, peut permettre à conserver la chaleur à l’intérieur du logement.</p>
						<br>
						<p><strong>Le soleil est une source naturelle de chaleur</strong>, autant profiter de cette source qui avec ces simples gestes au quotidien permettront de limiter la compensation par un <strong>système de chauffage</strong> qui consomme de l’énergie.</p>
						
						<br><center><img src="images/archives/img.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Mettre en place <strong>un interrupteur connecté</strong> ou des <strong>micromodules domotiques</strong> derrière les commutateurs de vos volets roulants peuvent donc faire sens et accompagner cet effort de manière efficace. Au-delà du fait de simplifier la gestion de vos volets ou de les centraliser en un même point, cette <strong>gestion intelligente</strong> associée à un capteur de luminosité et/ou d’un état du jour qu’un système <strong>domotique</strong> est capable de gérer dynamiquement sans besoin de matériel supplémentaire, peut vous aider à générer des économies de chauffage.</p>
						<br>
						<br>
						
						<h2 id="section_further_down_6"><strong>Exemple d’une automatisation sous le système C.L.A.R.A avec Smartlife.</strong></h2>
						
						<p>L’automatisation permet sous <strong>Smartlife</strong> permet de piloter l’<strong>ouverture des volets</strong> exposés au sud, si les données météo indiquent un ensoleillement de la position de la maison.</p>
						<br>
						<p>Et dans la même logique, on pourra jouer sur la <strong>fermeture automatique</strong> en se basant sur l’heure du couché de soleil.</p>
						<br>
						<p>Là encore il existe plusieurs solutions et sous plusieurs formes et plusieurs protocoles. Il ne reste qu’à faire le choix du module qui correspond à votre installation. Z-wave, Zigbee ou Wi-Fi, il y a en a pour toutes les installations et tous les budgets.</p>
						<br>
						<br>

						<h2 id="section_further_down_7"><strong>Le thermostat connecté pour réguler efficacement le chauffage tout en restant dans la limite des 19 degrés.</strong></h2>

						<p>La ligne de conduite est claire, l’état ne cesse de répéter qu’il faudrait un effort collectif également autour du chauffage qui est un poste énergétique important qui pèse dans la balance de la consommation en hiver. Réduire la consigne de température d’un degré permet de réaliser <strong>une économie de 7% sur la facture</strong>. En passant de 21°C à 19°C on peut alors espérer un gain intéressant, sans forcément le ressentir dans la maison. Mais pour que cette baisse de consigne soit la plus transparente possible d’un point de vue confort, il faut obligatoirement l’associer à une régulation intelligente du système de chauffage.</p>

						<br><center><img src="images/archives/img.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Le <strong>thermostat connecté</strong> permet cette <strong>régulation dynamique et intelligente</strong>. Si un thermostat traditionnel le permet aussi dans une certaine mesure, il ne tient pas compte des autres éléments qui vont permettre une optimisation fine et précise de la régulation au quotidien. Un thermostat connecté, de part sa connectivité est capable d’aller chercher d’autres données cruciales qui lui permettront d’ajuster sa régulation et par conséquent de diminuer la consommation.</p>
						<br>
						<p>En se basant sur des données telles que la météo pour anticiper les écarts de températures ou encore votre présence au domicile pour en réduire automatiquement la consigne pendant vos phases d’absence.</p>
						<br>
						<p>Le <strong>thermostat connecté</strong> a aussi un autre avantage, celui de disposer d’une <strong>interface de configuration simplifiée et assistée</strong>. Combien d’entre vous lisent ces lignes et n’ont jamais tout compris aux différentes touches et fonctions de leur thermostat de chauffage, se contentant au strict minimum … Le thermostat connecté se configure depuis une application mobile et offre de nombreux assistants pour vous mâcher le travail. Vous n’aurez donc pas à vous creuser la tête pour disposer de la meilleure régulation pour votre maison.</p>
						<br>
						<p><strong>Facilement connectable à la domotique</strong>, un assistant vocal ou d’autres services cloud, celui-ci s’alimente d’autres sources d’informations pour améliorer continuellement la consommation tout en évitant le coup de chaud ou le coup de froid… Soit un confort optimal, une réduction de la facture et une participation à l’effort collectif.</p>
						<br>
						<p>Notez que les thermostats connectés peuvent faire l’objet d’une réduction d’impôts si vous en équipez votre logement. De quoi compenser une partie de l’investissement.</p>
						<br>
						<p>Il existe plusieurs thermostats connectés sur le marché. Notez qu’il existe aussi des <a href=""><font color="blue"><strong>vannes thermostatiques connectées</strong></font></a> qui permettront de réguler la température des chambres efficacement tout en favorisant le réchauffement de la pièce de vie.</p>
						<br>
						<br>

						<h2 id="section_further_down_8"><strong>Limiter la consommation d’eau dans la douche de manière simple et visuelle.</strong></h2>

						<p>Si vous avez des enfants, vous aurez sans doute remarqué que du côté de la douche, il y a sans doute quelques efforts à faire également pour <strong>limiter le gaspillage d’eau</strong>… Cela vaut aussi pour les plus grands, même si nos enfants remportent la palme sur ce poste. Alors quoi de mieux et plus efficace qu’un code couleur pour indiquer et faire prendre conscience très simplement de la quantité d’eau qui s’écoule ?</p>
						<br>
						<p>Avec <strong>le pommeau Hydrao</strong>, l’internet des objets a vu fleurir une belle invention qui sans grande technicité, propose un moyen efficace et universel de comprendre qu’il faut écourter la douche et commencer à arrêter le gaspillage.</p>
						
						<br><center><img src="images/archives/img.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Le pommeau est équipé d’un turbine de comptage et de LED pour indiquer à l’utilisateur une couleur différente par palier de litres d’eau écoulés. Simple, visuelle et efficace, la connectivité permet également en approchant un smartphone du pommeau d’en connaître la consommation en eau.</p>
						<br>
						<br>

						<h2 id="section_further_down_9"><strong>L’autoproduction solaire pour compenser une partie de sa consommation électrique.</strong></h2>
						
						<p>C’est la dernière innovation phénomène de ces derniers mois. La possibilité pour tous de commencer à produire rapidement et simplement une petite quantité d’électricité qui est consommée directement par le foyer. <strong>Pas de travaux lourds</strong>, pas de déclaration particulière, un simple <strong>kit à poser et à brancher sur une prise de la maison</strong> et c’est tout !</p>
						<br>
						<p>Si ce genre de kit n’a pas pour vocation à vous rendre autonome en électricité, il permet toutefois de compenser une partie de votre consommation (talon de consommation), et par conséquent vous aider à réduire votre consommation, sans la changer ! Alors si à cela vous ajoutez les quelques conseils distillés un peu plus haut dans ce guide, vous verrez forcément votre facture réduire et votre participation active à l’effort collectif vis à vis de la sobriété énergétique.</p>

