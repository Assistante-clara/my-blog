						<h2 id="section_further_down_0"><strong>La sobriété énergétique, ça commence par des gestes simples du quotidien.</strong></h2>
						
						<p><strong>Éteindre la lumière</strong> quand on quitte une pièce, <strong>ouvrir les volets</strong> au lieu d’allumer la lumière, <strong>fermer les volets</strong> juste avant la tombée de la nuit pour conserver la chaleur à l’intérieur, <strong>réduire la consigne de chauffage</strong> dans les chambres et favoriser la pièce de vie, <strong>réduire le temps passé sous la douche</strong> ou <strong>couper les appareils en veille</strong>… Ce sont tant de gestes simples qui peuvent rapidement et de manière transparente faire <strong>réduire notre consommation</strong> et par conséquent participer à cet effort collectif.</p>
						<br>
						<p>Mais saviez-vous que <strong>la domotique</strong> et les <strong>objets connectés</strong> au travers de <strong>règles</strong> et <strong>scénarios</strong> simples permettent de faire tout cela pour vous ? Voyons quelques exemples de mise en pratique avec des périphériques que vous possédez certainement déjà ! Alors si en plus on peut mettre à profit ces équipements pour participer à cet effort sans devoir investir et forcer la génération d’une dépense supplémentaire tant sur le point financier que sur le bilan carbone, c’est une démarche « Win/Win » où tout le monde est gagnant !</p>
						<br>
						<br>
						
						<h2 id="section_further_down_1"><strong>La prise connectée pour couper l’alimentation des appareils en un geste.</strong></h2>
						
						<p>Nous l’avons vu dans notre série <strong>Smartlife</strong> plusieurs tests et guides autour de la consommation d’énergie, la prise connectée est l’objet connecté qui se révèle sans doute le plus utile dans cette démarche, tout en restant le plus économique. En effet, comme nous l’avons démontré au travers d’un guide que vous retrouvez <a href=""><font color="blue"><strong>ici</strong></font><a>, une prise connectée embarquant la mesure de consommation permet très facilement via le <strong>système domotique</strong> <a href=""><font color="blue"><strong>Smartlife</strong></font></a>, d’analyser et comprendre la courbe de consommation d’un appareil. En fonctionnement, mais également en veille. L’analyse exemple de notre guide démontre très clairement qu’<strong>un ordinateur, même en veille peut consommer beaucoup d’énergie</strong>…</p>
						<br>
						<p>Cela vaut pour de nombreux appareils de la maison qui, plutôt que de passer en veille devraient directement voir leur alimentation coupée pour consommer 0 Watt dans ces phases totalement inutiles. <strong>La prise connectée</strong> est donc le parfait équipement pour répondre à cette problématique de manière automatique, simple et sans faille. On sait tous que si l’on compte sur nous pour assurer cette tâche, cela ne durera qu’un temps, ou alors il y aura des ratés et nous ne penserons pas toujours à débrancher le chargeur du smartphone, la TV ou d’autres appareils du quotidien quand nous ne les utilisons pas.</p>
						<br>
						<p>Avec <strong>la prise connectée</strong> plus de problème. Un simple et rapide scénario qui va <strong>mesurer la consommation</strong> de l’appareil va être capable de couper la prise quand la consommation détectée sera inférieure à un certain seuil. Cela indiquant alors que l’appareil est en veille.</p>
						<br>
						<br>

