						<p>Depuis quelques jours le gouvernement ne cesse de prévenir la population française qu’il va falloir collectivement réduire la consommation d’énergie de sorte à ce que les restrictions, voire que les coupures soient évitées. En effet, la situation extrêmement tendue entre les marchés financiers, la guerre en Ukraine, les efforts écologiques à fournir rapidement et l’été record enregistré ont généré une tension extrême sur l’énergie, et cela sans parler des très nombreux réacteurs nucléaires à l’arrêt pour des raisons de maintenance.</p>
						<br>
						<p>Le constat est sans équivoque, dans l’hypothèse où nous aurions un hiver aussi extrême que l’été que nous venons de passer, il est fort à parier qu’<strong>il n’y aura pas assez d’énergie pour alimenter l’ensemble des foyers</strong> et des entreprises pendant les phases de pic cet hiver. Il nous faut <strong>donc agir au plus vite tant qu’il est encore temps</strong> et même si nous avons brulé la chandelle par les deux bouts pendant toutes ces années.</p>
						<br>
						<p>Avant que l’état vienne à <strong>nous dicter des recommandations pour réduire sa facture</strong> avec des gestes simples, voyons, de notre point de vu de domoticiens, comment mettre à profit nos <strong>systèmes domotiques</strong>, voire nos <strong>objets connectés</strong> dans cette réduction de la consommation, <strong>sans forcément se restreindre</strong>.</p>
