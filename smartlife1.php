						<h1>Tutoriel Installer une ampoule avec Smart Life</h1>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-user"></i> Admin</span> <span><i class="fa fa-clock-o"></i> 1 juillet 2019</span> <span><i class="fa fa-comment"></i> 4 comments</span> <span><i class="fa fa-folder"></i> Smartlife</span></p>
						</div>
						
						<div class="clearfix post-meta">
							<p><span><i class="fa fa-clock-o"></i> Mise à jour : 1 Oct 2021</span></p>
						</div>

						<p>Aujourd’hui est un grand jour : vous venez de télécharger votre Assistante Clara (C.L.A.R.A 1 sur Win7, C.L.A.R.A 2 sur Win10+ ) !! Dézippage, installation et les premières questions / réponses.</p>
						<br>
						<p>C’est fabuleux mais vous voyez très vite un autre potentiel, d’ailleurs tout le monde en parle de la maison connectée ! Allumer et éteindre les lumières par la voix, changer leur couleur, fermer et ouvrir les volets sans lever le petit doigt… Le rêve. Mais voilà, c’est bien joli tout ça, mais comment faut-il faire ?</p>
						<br>
						<p>Nous allons nous intéresser aux ampoules connectées Wi-Fi autonomes.  En effet, il existe deux types d’ampoules connectées utilisables avec Clara :</p>
						<br>
						<p><strong>Les ampoules fonctionnant via un hub</strong> ou passerelle, ou encore pont en français. C’est le cas des Philipps Hue par exemple. C’est ce hub qui va gérer les ampoules et qui sera relié à Clara. Les ampoules utilisées devront être de la marque ou compatible.</p>
						<br>
						<p><strong>Les ampoules autonomes</strong>, qui fonctionnent seules. Plusieurs marques peuvent être utilisées dans une même pièce. Elles fonctionnent via le Wi-fi et une application mobile. Cette application doit être télécharger sur le smartphone et être reconnu par le plugin smartlife de Clara. C’est le cas de Smartlife, Yeelight…</p>
						<br>
						<p>La première étape va consister à télécharger l’application permettant de faire fonctionner l’ampoule. L’application est présentée soit sur la boite, soit dans le mode opératoire. Dans notre cas, ce sera Smartlife. Suite à l’installation, il est nécessaire de créer un compte  en suivant les différentes étapes (courriel, mot de passe…).</p>
						<br>
						<blockquote>
							<font color="green">Attention car dans le cas de Smartlife, il faut attendre quelques seconde (30 max) avant que votre compte soit réellement pris en compte par l’ensemble de Clara.</font>
						</blockquote>
						<br>
						<p>Il est parallèlement nécessaire de télécharger le plugin Smartlife pour Clara. La procédure est la même pour Yeelight ou autres.<font color="green">Attention mise à jour le plugin de Clara, le plugin Smartlife est en racine dans l'I.A.</font></p>
						
						<br><center><img src="images/smartlife/smartlife 1/img 1.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Vient ensuite la mise en place de l’ampoule. Rien de plus simple : dévissez l’ancienne et mettez votre nouvelle acquisition à la place.</p>
						<br>
						<p>Ensuite, ce n’est pas beaucoup plus compliqué. Dans le cas de <strong>SmartLife</strong>, ouvrez l’application sur le téléphone et sélectionner le « + » pour mettre une nouvelle lampe en service. Faîtes ensuite jouer votre interrupteur pour éteindre et allumer la lampe 2 ou 3 fois (mais qu’elle reste au final en position allumé). <strong>La lampe va se mettre à clignoter.</strong></p>

						<br><center><img src="images/smartlife/smartlife 1/img 2.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Quand c’est le cas, sélectionnez « confirmé dans la lampe flash »… Ok, c’est pas vraiment français, mais bon en gros ça veut dire « <strong>confirmez que l’ampoule clignote</strong> ». Puis, <strong>entrez votre code Wi-Fi</strong>. La configuration se fait seule. Quand tout est fini, vous arrivez à une page vous donnant la possibilité (qu’il faut saisir!) de changer le nom de la lampe. C’est ce nom qui suivra votre ampoule pour les demandes vocales avec Clara. <strong>Restez donc simple et précis</strong>.</p>

						<br><center><img src="images/smartlife/smartlife 1/img 3.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Et voilà ! vous avez une pièce où votre lumière vous obéira ! Les mots d’activation sont assez simples :.</p>
						<br>
						<p>&rarr; Clara, allume la chambre</p>
						<p>&rarr; Clara, éteins la chambre</p>
						<p>&rarr; Clara, allume la chambre à 50%</p>
						<p>&rarr; Clara, allume la chambre en bleu</p>
						<br>
						<a href="jarvis.php"><font color="blue"><strong>Voir toutes les commandes Clara ici…</strong></font></a>
						<br>
						<br>
						<p>Vous pouvez aussi <a href=""><font color="blue"><strong>créer des routines</strong></font></a> pour éteindre toutes les lumières de la maison en même temps ou encore avoir une couleur particulière pour lire ou <a href=""><font color="blue"><strong>regarder la télévision…</strong></font></a> Mais cela est une autre histoire !</p>
						
						 

						<br><center><img src="videos/smartlife/smartlife 1/vid 1.mp4" width="50%" height="50%" alt=""/></center><br>
						
						<blockquote>
							<font color="green">En conclusion, nous sommes sur des produits qui restent d’un très très bon rapport qualité prix car tous à 9,95€. Le détecteur d’ouverture est excellent, le détecteur de mouvement est très bien mais l’alarme est plus un gadget qu’autre chose. Même si pour 9,95€ elle peut faire prendre peur à un cambrioleur du dimanche, elle est peu dissuasive du fait de sa conception.
							Mais finalement, n’hésitez pas, foncez ! Il n’y pas d’équivalent à ce rapport qualité prix en ce moment.</font>
						</blockquote> 

