						<p>Clara, comme tout assistant vocal qui se respecte, propose un certain nombre d’Easter Eggs. <strong>Mais qu’est-ce qu’un Easter Eggs?</strong></p>
						
						<br><center><img src="images/cmd/cmd/img 2.png" width="50%" height="50%" alt=""/></center><br>
						
						<p>Selon la définition de Wikipédia, un <a href="https://fr.wikipedia.org/wiki/Easter_egg" target="_blank" rel="noopener noreferrer"><strong><font color="blue">easter egg (ou « œuf de Pâques »)</font></strong></a> est, en informatique ou dans les jeux vidéo, une fonction cachée au sein d’un programme accessible grâce à un mot-clé ou à une combinaison de touches ou de clics.</p>
						<br>
						<p>Clara ne déroge pas à la règle et vous propose quelques petites surprises que nous vous invitons à découvrir en prononçant les phrases ci-dessous. En voici une sélection, que nous maintiendrons à jour au fur et à mesure des nouveautés découvertes. N’hésitez pas à nous communiquer les vôtre, nous les ajouterons ici.</p>
